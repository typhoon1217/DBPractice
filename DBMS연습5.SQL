DECLARE
    -- REFERENCE TYPE 변수선언(클래스타입)
    EMP_RECORD EMPLOYEES%ROWTYPE;
    ANNSAL NUMBER; -- 추가: 연봉을 계산할 변수

BEGIN
    SELECT * INTO EMP_RECORD
    FROM EMPLOYEES
    WHERE MANAGER_ID IS NULL AND FIRST_NAME = 'KING';
    
    IF EMP_RECORD.COMMISSION_PCT IS NULL THEN -- 수정: EMO_RECORD -> EMP_RECORD, COMMISSION_PCT -> COMMISSION_PCT
        EMP_RECORD.COMMISSION_PCT := 0; -- 수정: EMO_REOCORD -> EMP_RECORD
    END IF;
    
    ANNSAL := EMP_RECORD.SALARY * 12 + (EMP_RECORD.SALARY * EMP_RECORD.COMMISSION_PCT); -- 수정: COMMISTION_PCT -> COMMISSION_PCT
    DBMS_OUTPUT.PUT_LINE('아이디 / 이름 / 직군 /연봉'); 
    DBMS_OUTPUT.PUT_LINE('----------------------------');

    DBMS_OUTPUT.PUT_LINE(RPAD(EMP_RECORD.EMPLOYEE_ID, 15) || '/' || 
                         RPAD(EMP_RECORD.FIRST_NAME, 10) || '/' || 
                         RPAD(EMP_RECORD.JOB_ID, 15) || '/' || 
                         RPAD(TO_CHAR(ANNSAL,'$999999.99'),15)); -- 수정: EMP_RECORD.TO_CHAR -> TO_CHAR
END;
/
