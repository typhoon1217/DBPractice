DECLARE
    -- 배열 테이블 타입 정의
    TYPE FIRST_NAME_ARRAY_TYPE IS TABLE OF EMPLOYEES.FIRST_NAME%TYPE INDEX BY BINARY_INTEGER; 
    TYPE JOB_ID_ARRAY_TYPE IS TABLE OF EMPLOYEES.JOB_ID%TYPE INDEX BY BINARY_INTEGER;
    TYPE EMPLOYEE_ID_ARRAY_TYPE IS TABLE OF EMPLOYEES.EMPLOYEE_ID%TYPE INDEX BY BINARY_INTEGER;
    
    -- 테이블 타입 (배열 타입) 변수 선언
    FIRST_NAME_ARRAY FIRST_NAME_ARRAY_TYPE;
    JOB_ID_ARRAY JOB_ID_ARRAY_TYPE;
    EMPLOYEE_ID_ARRAY EMPLOYEE_ID_ARRAY_TYPE;
    I BINARY_INTEGER := 0; -- 루프 카운터 초기화
    
BEGIN
    -- 직원 테이블에서 데이터를 배열 타입 변수에 저장
    FOR K IN (SELECT FIRST_NAME, JOB_ID, EMPLOYEE_ID FROM EMPLOYEES) LOOP
        I := I + 1;
        FIRST_NAME_ARRAY(I) := K.FIRST_NAME;
        JOB_ID_ARRAY(I) := K.JOB_ID;
        EMPLOYEE_ID_ARRAY(I) := K.EMPLOYEE_ID;
    END LOOP;
    
    -- 저장된 데이터 출력
    DBMS_OUTPUT.PUT_LINE('이름/직업/사번');
    DBMS_OUTPUT.PUT_LINE('---------------');
    FOR J IN 1..I LOOP
        DBMS_OUTPUT.PUT_LINE(RPAD(FIRST_NAME_ARRAY(J),15) 
        || '/' || RPAD(JOB_ID_ARRAY(J),10) 
        || '/' || RPAD(EMPLOYEE_ID_ARRAY(J),10));
    END LOOP;
END;
/

